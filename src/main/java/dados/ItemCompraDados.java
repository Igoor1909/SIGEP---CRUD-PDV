
package dados;

public class ItemCompraDados {


    
    private int id_local, id_compra, id_produto, quantidade_produto;
    private double total_produto, desconto_produto;

    public int getId_local() {
        return id_local;
    }

    public void setId_local(int id_local) {
        this.id_local = id_local;
    }

    public int getId_compra() {
        return id_compra;
    }

    public void setId_compra(int id_compra) {
        this.id_compra = id_compra;
    }

    public int getId_produto() {
        return id_produto;
    }

    public void setId_produto(int id_produto) {
        this.id_produto = id_produto;
    }

    public int getQuantidade_produto() {
        return quantidade_produto;
    }

    public void setQuantidade_produto(int quantidade_produto) {
        this.quantidade_produto = quantidade_produto;
    }

    public double getTotal_produto() {
        return total_produto;
    }

    public void setTotal_produto(double total_produto) {
        this.total_produto = total_produto;
    }

    public double getDesconto_produto() {
        return desconto_produto;
    }

    public void setDesconto_produto(double desconto_produto) {
        this.desconto_produto = desconto_produto;
    }
    
    
    
    
}
